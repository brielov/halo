#[derive(Debug, PartialEq)]
pub enum Kind {
    Eof,
    Identifier,
    Unknown,

    // Errors
    InvalidChar,
    InvalidNumber,
    UnterminatedChar,
    UnterminatedString,

    // Primitives
    Binary,
    Char,
    Float,
    Hex,
    Integer,
    Octal,
    String,

    // Keywords
    Async,
    Break,
    Defer,
    Enum,
    Extend,
    False,
    Fn,
    For,
    In,
    Let,
    Loop,
    Match,
    Mut,
    Proto,
    Pub,
    Struct,
    True,
    Use,
    While,
    With,
    Yield,

    // Delimiters
    LBrace,
    LBracket,
    LParen,
    RBrace,
    RBracket,
    RParen,

    // Punctuation
    And,
    AndAnd,
    AndEq,
    At,
    Caret,
    CaretEq,
    Colon,
    Comma,
    Dot,
    DotDot,
    DotDotEq,
    Eq,
    EqEq,
    Ge,
    Gt,
    Le,
    Lt,
    Minus,
    MinusEq,
    Ne,
    Not,
    Or,
    OrEq,
    OrOR,
    PathSep,
    Percent,
    PercentEq,
    Plus,
    PlusEq,
    Question,
    RArrow,
    Semi,
    Shl,
    ShlEq,
    Shr,
    ShrEq,
    Slash,
    SlashEq,
    Star,
    StarEq,
    Tilde,
}

#[derive(Debug, PartialEq)]
pub struct Token<'a> {
    pub kind: Kind,
    pub lexeme: &'a str,
    pub start: usize,
    pub end: usize,
}
